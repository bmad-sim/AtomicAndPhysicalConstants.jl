

module ParticleSpecies
using Main.PhysicalConstants

abstract type AbstractSpecies end

#=
Here's an immutable struct to 
store the qualities of the 
reference particle for tracking
=#
struct RefSpecies <: AbstractSpecies
  name::String
  charge::Int32
  mass::Float64
  other::Dict{String, Float64}
end; export RefSpecies

#= 
Here I've defined a type which contains all
reasonable information about the subatomic
particles: I've used this to get rid of 
some of the arrays in the fortran code.
=#
mutable struct SubatomicSpecies <: AbstractSpecies
  name::String                      # common name of the particle
  charge::Int32                     # charge on the particle in units of e+
  mass::Float64                     # mass of the particle in [eV/c^2]
  anomalous_moment::Float64         # anomalous magnetic moment 
  spin::Float16                     # spin magnetic moment in [ħ]
end; export SubatomicSpecies





