#=
AtomicSpecies is a type that contains all the
information available to Bmad about a 
particular element, including the mass 
of each of its known isotopes
=#
mutable struct AtomicSpecies <: AbstractSpecies
  Z::Int32                      # number of protons
  name::String                  # periodic table element symbol
  i_offset::Int32               # honestly I'm not quite sure what this is, ask david
  mass::Dict{Int32,Float64}     # a dict to store the masses, keyed by isotope
                                #=
                                keyvalue -1 => average mass of common isotopes,
                                keyvalue n ∈ {0} ∪ N is the number of neutrons
                                      which gives the mass value
                                =#
end; export AtomicSpecies







